func (s *Services) medicineFromRequest(req models.MedicineRequest) models.Medicine {
	exp, _ := util.ParseDate(derefStr(req.ExpiryDate))
	mfg, _ := util.ParseDate(derefStr(req.ManufacturingDate))
	status := req.Status
	if status == "" {
		status = models.MedicineActive
	}
	return models.Medicine{
		MedicineCode: req.MedicineCode, MedicineName: req.MedicineName, CategoryID: req.CategoryID,
		Brand: req.Brand, BatchNumber: req.BatchNumber, ExpiryDate: exp, ManufacturingDate: mfg,
		HSNCode: req.HSNCode, GSTPercent: util.DerefFloat(req.GSTPercent, 0),
		PurchasePrice: req.PurchasePrice, SellingPrice: req.SellingPrice, MRP: req.MRP,
		DiscountPercent: util.DerefFloat(req.DiscountPercent, 0), RackNumber: req.RackNumber,
		MinimumStock: util.DerefInt(req.MinimumStock, 10), CurrentStock: util.DerefInt(req.CurrentStock, 0),
		Barcode: req.Barcode, SupplierID: req.SupplierID, Status: status,
	}
}

func mapMedicines(items []models.Medicine) []models.MedicineResponse {
	out := make([]models.MedicineResponse, len(items))
	for i, m := range items {
		catName, supName := "", ""
		if m.Category != nil {
			catName = m.Category.Name
		}
		if m.Supplier != nil {
			supName = m.Supplier.SupplierName
		}
		out[i] = models.MedicineResponse{
			ID: m.ID, MedicineCode: m.MedicineCode, MedicineName: m.MedicineName,
			CategoryName: catName, CategoryID: m.CategoryID, Brand: m.Brand, BatchNumber: m.BatchNumber,
			ExpiryDate: m.ExpiryDate, ManufacturingDate: m.ManufacturingDate, HSNCode: m.HSNCode,
			GSTPercent: m.GSTPercent, PurchasePrice: m.PurchasePrice, SellingPrice: m.SellingPrice,
			MRP: m.MRP, DiscountPercent: m.DiscountPercent, RackNumber: m.RackNumber,
			MinimumStock: m.MinimumStock, CurrentStock: m.CurrentStock, Barcode: m.Barcode,
			SupplierName: supName, SupplierID: m.SupplierID, Status: m.Status,
		}
	}
	return out
}

func derefStr(p *string) string {
	if p == nil {
		return ""
	}
	return *p
}

