func (s *Services) GetCategories(search string) ([]models.CategoryResponse, error) {
	var cats []models.Category
	q := s.DB.Order("name")
	if search != "" {
		q = q.Where("name LIKE ?", "%"+search+"%")
	}
	if err := q.Find(&cats).Error; err != nil {
		return nil, err
	}
	out := make([]models.CategoryResponse, len(cats))
	for i, c := range cats {
		out[i] = models.CategoryResponse{ID: c.ID, Name: c.Name, Description: c.Description}
	}
	return out, nil
}

