func now() time.Time { return time.Now() }

func (s *Services) Login(req models.LoginRequest) (*models.JwtResponse, error) {
	var user models.User
	if err := s.DB.Preload("Roles").Where("username = ?", req.Username).First(&user).Error; err != nil {
		return nil, badRequest("Invalid username or password")
	}
	if !user.Enabled || !user.AccountNonLocked {
		return nil, badRequest("Account is disabled or locked")
	}
	if !auth.CheckPassword(user.Password, req.Password) {
		return nil, badRequest("Invalid username or password")
	}
	jwt := auth.NewJWTManager("") // token generated by handler with real secret
	_ = jwt
	return &models.JwtResponse{
		Type:     "Bearer",
		ID:       user.ID,
		Username: user.Username,
		FullName: user.FullName,
		Roles:    user.RoleNames(),
	}, nil
}

