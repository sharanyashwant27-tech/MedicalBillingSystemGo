func (s *Services) GetAllUsers() ([]models.UserResponse, error) {
	var users []models.User
	if err := s.DB.Preload("Roles").Find(&users).Error; err != nil {
		return nil, err
	}
	out := make([]models.UserResponse, len(users))
	for i, u := range users {
		out[i] = toUserResponse(u)
	}
	return out, nil
}

func (s *Services) CreateUser(req models.UserRequest, adminUsername string) (*models.UserResponse, error) {
	hash, _ := auth.HashPassword(req.Password)
	var roles []models.Role
	s.DB.Where("name IN ?", req.Roles).Find(&roles)
	user := models.User{
		Username: req.Username, Password: hash, FullName: req.FullName,
		Email: req.Email, Phone: req.Phone, Roles: roles, Enabled: true, AccountNonLocked: true,
		CreatedAt: now(),
	}
	if err := s.DB.Create(&user).Error; err != nil {
		return nil, badRequest("Username already exists")
	}
	id := user.ID
	s.LogAudit("CREATE", "User", &id, adminUsername, "Created user: "+user.Username, "")
	r := toUserResponse(user)
	return &r, nil
}

func (s *Services) UpdateUser(req models.UserUpdateRequest, adminUsername string) (*models.UserResponse, error) {
	var user models.User
	if err := s.DB.Preload("Roles").First(&user, req.UserID).Error; err != nil {
		return nil, notFound("User not found")
	}
	user.Username = req.Username
	user.FullName = req.FullName
	user.Email = req.Email
	user.Phone = req.Phone
	if req.Password != "" {
		hash, _ := auth.HashPassword(req.Password)
		user.Password = hash
	}
	var roles []models.Role
	s.DB.Where("name IN ?", req.Roles).Find(&roles)
	s.DB.Model(&user).Association("Roles").Replace(roles)
	user.UpdatedAt = now()
	s.DB.Save(&user)
	id := user.ID
	s.LogAudit("UPDATE", "User", &id, adminUsername, "Updated user: "+user.Username, "")
	r := toUserResponse(user)
	return &r, nil
}

